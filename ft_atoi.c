#include "libft.h"

int	ft_atoi(const char *s)
{
	long	num;
	int		sign;

	num = 0;
	sign = 1;
	while (*s == '\t' || *s == '\n' || *s == '\v'
		|| *s == '\f' || *s == '\r' || *s == ' ')
		s++;
	if (*s == '-' || *s == '+')
		sign -= (*s++ == '-') * 2;
	while (*s >= '0' && *s <= '9')
		num = num * 10 + (*s++ - '0');
	return ((int)(num * sign));
}
